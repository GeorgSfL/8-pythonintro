mood = input("Do you feel happy, nervous, sad, excited, relaxed or how are you up today?")
if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("The sun will shine again :)")
elif mood == "excited":
    print("Woohoo!")
elif mood == ("relaxed"):
    print("Stay cool.")
else:
    print("Cheer up, mate!")
